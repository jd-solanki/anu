<script lang="ts" setup>
import type { AChipEvents, aChipSlots } from './meta'
import { aChipProps } from './meta'
import { useLayer } from '@/composables/useLayer'

const props = defineProps(aChipProps)
const emit = defineEmits<AChipEvents>()
defineSlots<typeof aChipSlots>()

defineOptions({
  name: 'AChip',
})

const attrs = useAttrs()

const { getLayerClasses } = useLayer()

const isClickable = computed(() => attrs.onClick !== undefined)

const { styles, classes } = getLayerClasses(
  toRef(props, 'color'),
  toRef(props, 'variant'),
  isClickable,
)

function closeChip() {
  emit('update:modelValue', false)
  emit('click:close')
}
</script>

<template>
  <div
    v-if="props.modelValue"
    :style="styles"
    class="a-chip"
    :class="[
      {
        'a-chip-disabled': props.disabled,
        'cursor-pointer': isClickable,
      },
      classes,
    ]"
  >
    <!-- Prepend icon -->
    <i
      v-if="props.icon"
      :class="props.icon"
    />

    <!-- Default slot -->
    <slot />

    <!-- Append icon -->
    <i
      v-if="props.appendIcon"
      :class="props.appendIcon"
    />

    <!-- Close icon -->
    <i
      v-if="props.closable"
      class="i-bx-x hover:i-bx-bxs-x-circle hover:opacity-70"
      @click="closeChip"
    />
  </div>
</template>
